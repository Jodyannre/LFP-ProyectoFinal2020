class pila (object):
    '''
    __________________________________________________________________________________
    ► Descripción:
    
    → Clase que contiene las funciones de la pila.
    __________________________________________________________________________________
    '''
    def __init__ (self):
        '''
        __________________________________________________________________________________
        ► Descripción:
        
        → Método que inicializa la pila.
        __________________________________________________________________________________
        ► Parámetros:
        
            (Sin parámetros)
        __________________________________________________________________________________
        ''' 
        self.objetos = []
    
    def push(self,objeto):
        '''
        __________________________________________________________________________________      
        ► Descripción: 
        
        → Método push de la pila.             
        __________________________________________________________________________________    
        ► Parámetros:                                                                      
                                                                                          
        • objeto = Valor a guardar en la pila.
        __________________________________________________________________________________
        '''
        self.objetos.append(objeto)
        
    def pop(self):
        '''
        __________________________________________________________________________________      
        ► Descripción: 
        
        → Método pop de la pila.             
        __________________________________________________________________________________    
        ► Parámetros:                                                                      
                                                                                          
            (Sin parámetros)
        __________________________________________________________________________________
        '''
        return self.objetos.pop()
        
    def tamaño(self):
        '''
        __________________________________________________________________________________      
        ► Descripción: 
        
        → Método que retorna el tamaño de la pila.            
        __________________________________________________________________________________    
        ► Parámetros:                                                                      
                                                                                          
            (Sin parámetros)
        __________________________________________________________________________________
        '''
        return len(self.objetos)
        
    def vacia(self):
        '''
        __________________________________________________________________________________      
        ► Descripción: 
        
        → Método que verifica si la pila está vacia.            
        __________________________________________________________________________________    
        ► Parámetros:                                                                      
                                                                                          
            (Sin parámetros)
        __________________________________________________________________________________
        '''
        return self.objetos == []
        
        